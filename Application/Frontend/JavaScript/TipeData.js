//tipe data number, pada javascript integer dan float jadi satu
document.writeln(100);
document.writeln("<br>");
document.writeln(100.123);
document.writeln("<br>");
document.writeln("<hr>");

//number notation, hexa, binary = "0b", octal
document.writeln(0b10101010);
document.writeln("<br>");
document.writeln("<hr>");

//boolean, True and False
document.writeln(true);
document.writeln("<br>");
document.writeln(false);
document.writeln("<br>");
document.writeln("<hr>");

//String -> untuk text
document.writeln("Text Sring :" + "Apa?");
document.writeln("<br>");
document.writeln("<hr>");

//Escape Sequence
document.writeln("<textarea cols='100' rows='10'>");
document.write("Hello\nHello\n");
document.write("Hello\tHello\n");
document.writeln("Hello");
document.writeln("Hai");
document.writeln("Hai\\")
document.writeln("Hai\'")
document.writeln("Hai\"")
document.writeln("</textarea>");
document.writeln("<br>");
document.writeln("<hr>");
